/*
 * Muestra los números múltiplos de 5 de 0 a 100 utilizando un bucle while.
 * Author:Pablo Camino Vázquez
 */
public class Cap5Ejercicio02 {
    public static void main(String[] args) {
        int n = 5 ;
        int i = 1;
        while (i<=20){
            System.out.println(n*i);
            i++;
        }
    }
}
